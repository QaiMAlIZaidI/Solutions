function cityCountry(city: string, country: string) {
  return `${city}, ${country}`;
}

let result1 = cityCountry('Lahore', 'Pakistan');
let result2 = cityCountry('New York', 'USA');
let result3 = cityCountry('Tokyo', 'Japan');

console.log(result1);
console.log(result2);
console.log(result3);